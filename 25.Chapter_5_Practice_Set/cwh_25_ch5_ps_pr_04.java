import java.util.*;

public class cwh_25_ch5_ps_pr_04 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i=10; i>0; i--) {
            System.out.println(n + " * " + i + " = " + (n*i));
        }
    }
}